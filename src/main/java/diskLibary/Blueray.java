package diskLibary;



/**
 * The purpose of this class is to represent a Blueray.
 * 
 * 
 * @author Anthony Smith
 */

public class Blueray extends Disk
{
   public Blueray()
   {  
       super();
   }
   
   public Blueray(String name, String description)
   {  
       super.name = name;
       super.description = description;
   }

} 
